// CertForRegion returns the x509.Certificate for a region
func CertForRegion(region string) (*x509.Certificate, error) {
	certsMu.RLock()
	c, ok := certs[region]
	certsMu.RUnlock()
	if !ok {
		return loadcert(region)
	}
	return c, nil
}

func loadcert(region string) (*x509.Certificate, error) {
	certsMu.Lock()
	defer certsMu.Unlock()
	var c *x509.Certificate
	switch region {
	case "eu-west-1":
		c = parseCert(euwest1)
	case "eu-central-1":
		c = parseCert(eucentral1)
	case "sa-east-1":
		c = parseCert(saeast1)
	case "us-east-1":
		c = parseCert(useast1)
	case "ap-northeast-1":
		c = parseCert(apnortheast1)
	case "us-west-2":
		c = parseCert(uswest2)
	case "us-west-1":
		c = parseCert(uswest1)
	case "ap-southeast-1":
		c = parseCert(apsoutheast1)
	case "ap-southeast-2":
		c = parseCert(apsoutheast2)
	default:
		return nil, ErrInvalidRegion
	}
	certs[region] = c
	return c, nil
}

func parseCert(cert string) *x509.Certificate {
	// We should panic here because we're loading a known dataset - if it
	// doesn't parse, something is real bad
	p, _ := pem.Decode([]byte(cert))
	c, err := x509.ParseCertificate(p.Bytes)
	if err != nil {
		panic(err)
	}
	return c
}
